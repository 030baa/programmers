SELECT SUM(SCORE) AS SCORE,	EMP_NO,	EMP_NAME,	POSITION,	EMAIL
FROM HR_EMPLOYEES
LEFT JOIN HR_DEPARTMENT USING(DEPT_ID)
LEFT JOIN HR_GRADE USING(EMP_NO)
GROUP BY EMP_NO
HAVING SUM(SCORE) = (SELECT SUM(SCORE)
    FROM HR_GRADE
    WHERE YEAR  = 2022
    GROUP BY EMP_NO
    ORDER BY SUM(SCORE) DESC 
    LIMIT 1)






